def reset_availability():
    """
    Reset available_copies = total_copies for EVERY book in the catalogue.
    Run this whenever books appear stuck as 'Unavailable' after testing.
    """
    db = get_db()
    result = db.books.update_many(
        {},
        [{"$set": {"available_copies": "$total_copies"}}],
    )
    print(f"Reset availability: {result.modified_count} books updated.")


def seed(reset=False):
    db = get_db()
    ensure_indexes()

    if reset:
        print("Resetting available_copies for all existing books…")
        reset_availability()

    inserted = 0
    skipped  = 0
    for book in SAMPLE_BOOKS:
        try:
            db.books.insert_one(book)
            inserted += 1
            print(f"  + {book['title']}")
        except Exception as e:
            if "duplicate" in str(e).lower() or "E11000" in str(e):
                # Book already exists — update availability, tags, genre and description
                update_fields = {
                    "available_copies": book["total_copies"],
                    "subject_tags": book["subject_tags"],
                }
                # Only set genre / description if provided (non-empty)
                if book.get("genre"):
                    update_fields["genre"] = book["genre"]
                if book.get("description"):
                    update_fields["description"] = book["description"]
                db.books.update_one(
                    {"isbn": book["isbn"]},
                    {"$set": update_fields},
                )
                skipped += 1
                print(f"  ~ {book['title']} (exists — refreshed)")
            else:
                print(f"  x {book['title']}: {e}")

    print(f"\nDone. {inserted} inserted, {skipped} refreshed.")


